    private void consultaAvancada() {
        String sql = "select idclientes as 'CÃ³digo', nomeCliente as 'Nome', cpfcnpj as 'CPF / CNPJ' from clientes where nomeCliente like ?";
        try {
            pst = conexao.prepareStatement(sql);
            // Passando o conteudo do campo pesquisar para o SQL
            pst.setString(1, "%" + txtPesquisaCliVendas.getText() + "%");
            rs = pst.executeQuery();
            // A linha abixo usa a rs2xml.jar 
            tblPesquisaCliVendas.setModel(DbUtils.resultSetToTableModel(rs));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);

        }
    }

    
    public void setarcampos() {
        
        int setar = tblPesquisaCliVendas.getSelectedRow();
        TelaVendas.txtPesquisaCliVendas.setText(tblPesquisaCliVendas.getModel().getValueAt(setar, 0).toString());
        TelaVendas.lblNomeCliente.setText(tblPesquisaCliVendas.getModel().getValueAt(setar, 1).toString());
        TelaVendas.txtPesquisaCliVendas.setEnabled(true);
        this.dispose();
    }
    
